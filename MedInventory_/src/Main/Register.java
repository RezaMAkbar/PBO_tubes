    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) throws SQLException {
        MysqlDataSource dataSource = new MysqlDataSource();
        String DB_URL = "jdbc:mysql://localhost:3306/tubes_pbo?serverTimezone=Asia/Jakarta";
        String DB_USERNAME = "root";
        String DB_PASSWORD = "";

        dataSource.setUrl(DB_URL);
        dataSource.setUser(DB_USERNAME);
        dataSource.setPassword(DB_PASSWORD);

        try (Connection conn = dataSource.getConnection()) {
            String username = usernameField.getText();
            String name = nameField.getText();
            String email = emailField.getText();
            String password = String.valueOf(passwordField.getPassword()); // Correct way to get the password

            if (username.isEmpty() || password.isEmpty() || name.isEmpty() || email.isEmpty()) {
                // Display an error message or take appropriate action
                JOptionPane.showMessageDialog(this, "Username or Name or Email or Password cannot be empty", "Error", JOptionPane.ERROR_MESSAGE);
                return; // Exit the method without proceeding to the next step
            }

            // Check if an email already exists
            if (isEmailExists(conn, email)) {
                JOptionPane.showMessageDialog(this, "Email already exists. Please use a different email.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String hashedPassword = BCrypt.hashpw(password, BCrypt.gensalt());
            String queryAdd = "INSERT INTO users (username, name, email, password, created_at)" +
                    "VALUES (?, ?, ?, ?, NOW())";
            try (PreparedStatement psAdd = conn.prepareStatement(queryAdd)) {
                psAdd.setString(1, username);
                psAdd.setString(2, name);
                psAdd.setString(3, email);
                psAdd.setString(4, hashedPassword);

                psAdd.executeUpdate();
            }

            TableObat mainPage = new TableObat();
            mainPage.setVisible(true);
            this.dispose();

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_registerButtonActionPerformed

